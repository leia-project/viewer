
<p>
	Sweco heeft een set maatregelen bepaald om de infrastructuur in Zeeland te verbeteren met het oog op grootschalige evacuaties tijdens een overstroming.
	Een deel van de maatregelen is toepasbaar in de fase ver voor de overstroming, zoals bijv. het ophogen of verbreden van wegen.
</p>
<p>
	Om een maatregel toe te passen, klik je op het icoontje in de tool of in de hoverbox.
</p>